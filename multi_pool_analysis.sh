#!/bin/bash
# ============================================================
#  多板块并行分析脚本
#  用法: ./multi_pool_analysis.sh
#  说明: 同时分析多个板块，适合周末全面扫描
# ============================================================

# 要分析的板块列表（可自定义）
POOLS=(
    "hgt"        # 沪股通
    "chip"       # 芯片
    "newenergy"  # 新能源
    "ai"         # 人工智能
)

echo "============================================================"
echo "  多板块分析  $(date '+%Y-%m-%d %H:%M')"
echo "  分析板块: ${POOLS[@]}"
echo "============================================================"
echo ""

for pool in "${POOLS[@]}"; do
    echo ""
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    echo "  开始分析: $pool"
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    ./daily_pool_analysis.sh "$pool"
    echo ""
done

echo "============================================================"
echo "  全部板块分析完成!  $(date '+%H:%M:%S')"
echo "============================================================"
